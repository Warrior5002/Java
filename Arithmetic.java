package UdemyJava;

public class Arithmetic {

    public static void main(String[] args) {
        int a=14, b=5;

        int c=a/b;
        int r=a%b;

        float q=(float)a/b;

        System.out.println(c);
        System.out.println(r);
        System.out.println(q);

        float e=14.3f, g=3.2f;

        float s=e%g;

        System.out.println(s);

        System.out.println(10+20/5);

        System.out.println((10+20)/5);



    }
}
